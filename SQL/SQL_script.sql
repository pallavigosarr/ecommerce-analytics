#4. Calculate the retention rate of customers, defined as the percentage of customers who make another purchase 
#within 6 months of their first purchase
WITH customer_orders AS (
    SELECT 
        c.customer_unique_id,
        o.order_id,
        o.order_purchase_timestamp,
        -- Find the first ever purchase date for this unique customer
        MIN(o.order_purchase_timestamp) OVER(PARTITION BY c.customer_unique_id) AS first_purchase_date
    FROM 
        orders o
    JOIN 
        customers c ON o.customer_id = c.customer_id
    WHERE 
        o.order_status NOT IN ('canceled', 'unavailable')
),
retention_flags AS (
    SELECT 
        customer_unique_id,
        -- Flag 1 if a secondary order exists within a strict 6-month (180 days) window
        MAX(CASE 
            WHEN DATEDIFF(order_purchase_timestamp, first_purchase_date) BETWEEN 1 AND 180 
            THEN 1 ELSE 0 
        END) AS retained_within_6m
    FROM 
        customer_orders
    GROUP BY 
        customer_unique_id
)
SELECT 
    COUNT(DISTINCT customer_unique_id) AS total_unique_customers,
    SUM(retained_within_6m) AS retained_customers_count,
    ROUND(100.0 * SUM(retained_within_6m) / COUNT(DISTINCT customer_unique_id), 2) AS retention_rate_percentage
FROM 
    retention_flags;

