#3. Calculate the year-over-year growth rate of total sales. 
WITH annual_revenue AS (
    SELECT 
        YEAR(o.order_purchase_timestamp) AS sales_year,
        SUM(oi.price) AS total_sales
    FROM 
        orders o
    JOIN 
        order_items oi ON o.order_id = oi.order_id
    WHERE 
        o.order_status NOT IN ('canceled', 'unavailable') -- Standard data cleaning
    GROUP BY 
        YEAR(o.order_purchase_timestamp)
)
SELECT 
    sales_year,
    ROUND(total_sales, 2) AS current_year_sales,
    ROUND(LAG(total_sales) OVER (ORDER BY sales_year), 2) AS previous_year_sales,
    ROUND(
        100.0 * (total_sales - LAG(total_sales) OVER (ORDER BY sales_year)) / 
        LAG(total_sales) OVER (ORDER BY sales_year), 
        2
    ) AS yoy_growth_rate_pct
FROM 
    annual_revenue
ORDER BY 
    sales_year;
