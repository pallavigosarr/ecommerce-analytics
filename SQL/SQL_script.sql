#3. Find the total sales per category. 
SELECT 
    p.product_category AS Product_Category,
    ROUND(SUM(oi.price), 2) AS Total_Sales,
    COUNT(oi.order_id) AS Total_Orders_Count
FROM 
    order_items oi
LEFT JOIN 
    products p ON oi.product_id = p.product_id
GROUP BY 
    product_category
ORDER BY 
    total_sales DESC;

