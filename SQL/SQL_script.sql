#2. Calculate the cumulative sales per month for each year. 
WITH monthly_sales AS (
    SELECT 
        YEAR(o.order_purchase_timestamp) AS sales_year,
        MONTH(o.order_purchase_timestamp) AS sales_month_num,
        MONTHNAME(o.order_purchase_timestamp) AS sales_month,
        SUM(oi.price) AS monthly_revenue
    FROM 
        orders o
    JOIN 
        order_items oi ON o.order_id = oi.order_id
    WHERE 
        o.order_status NOT IN ('canceled', 'unavailable') -- Excludes unfulfilled orders
    GROUP BY 
        YEAR(o.order_purchase_timestamp),
        MONTH(o.order_purchase_timestamp),
        MONTHNAME(o.order_purchase_timestamp)
)
SELECT 
    sales_year,
    sales_month,
    ROUND(monthly_revenue, 2) AS monthly_revenue,
    ROUND(
        SUM(monthly_revenue) OVER (
            PARTITION BY sales_year 
            ORDER BY sales_month_num
            ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW
        ), 2
    ) AS cumulative_yearly_sales
FROM 
    monthly_sales
ORDER BY 
    sales_year, 
    sales_month_num;

