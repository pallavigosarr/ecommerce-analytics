#2. Find the average number of products per order, grouped by customer city. 
WITH order_item_counts AS (
    SELECT 
        o.order_id,
        o.customer_id,
        COUNT(oi.product_id) AS total_products_in_order
    FROM 
        orders o
    JOIN 
        order_items oi ON o.order_id = oi.order_id
    GROUP BY 
        o.order_id, 
        o.customer_id
)
SELECT 
    c.customer_city,
    ROUND(AVG(oic.total_products_in_order), 2) AS avg_products_per_order,
    COUNT(DISTINCT oic.order_id) AS total_orders_placed
FROM 
    order_item_counts oic
JOIN 
    customers c ON oic.customer_id = c.customer_id
GROUP BY 
    c.customer_city
ORDER BY 
    avg_products_per_order DESC, 
    total_orders_placed DESC;

